// Definition:
// A Function with Arguments (Parameters) is a function that accepts
// values when it is called. These values are called arguments.
// They allow the function to work with different inputs.

// Syntax

function functionName(parameter1, parameter2) {
    // Code to execute
}

// Example 1

function greet(name) {
    console.log("Hello " + name);
}

greet("Dharmik"); // Hello Dharmik

// Example 2

function add(a, b) {
    console.log(a + b);
}

add(10, 20); // 30

// Example 3

function multiply(x, y) {
    console.log(x * y);
}

multiply(5, 4); // 20

// Example 4

function student(name, age) {
    console.log("Name: " + name);
    console.log("Age: " + age);
}

student("Rahul", 21);

// Output
// Name: Rahul
// Age: 21