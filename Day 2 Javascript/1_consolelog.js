// Definition:
// console.log() is a JavaScript method used to display messages,
// variables, or values in the browser's Console.
// It is mainly used for testing, debugging, and checking program output.

// Examples

console.log("Hello, World!");

let name = "Dharmik";
console.log(name);

let age = 20;
console.log("Age:", age);

let sum = 10 + 5;
console.log("Sum =", sum);

// Output:
// Hello, World!
// Dharmik
// Age: 20
// Sum = 15