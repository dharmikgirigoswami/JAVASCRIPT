// Definition:
// Unary Operators are operators that work with only one operand
// (one variable or one value).

// Examples

let x = 10;

// Unary Plus (+)
let a = +x;
console.log(a); // 10

// Unary Minus (-)
let b = -x;
console.log(b); // -10

// Increment (++)
x++;
console.log(x); // 11

// Decrement (--)
x--;
console.log(x); // 10

// Logical NOT (!)
let isStudent = true;
console.log(!isStudent); // false

// typeof Operator
console.log(typeof x); // "number"

// Common Unary Operators
// +        Unary Plus
// -        Unary Minus
// ++       Increment
// --       Decrement
// !        Logical NOT
// typeof   Returns the data type