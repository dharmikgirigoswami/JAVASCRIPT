// Definition:
// Assignment Operators are used to assign values to variables.
// They can also perform an operation and then assign the result.

// Examples

let x = 10;    // Assign

x += 5;        // x = x + 5
console.log(x); // 15

x -= 3;        // x = x - 3
console.log(x); // 12

x *= 2;        // x = x * 2
console.log(x); // 24

x /= 4;        // x = x / 4
console.log(x); // 6

x %= 4;        // x = x % 4
console.log(x); // 2

x **= 3;       // x = x ** 3
console.log(x); // 8

// Common Assignment Operators
// =   Assign
// +=  Add and Assign
// -=  Subtract and Assign
// *=  Multiply and Assign
// /=  Divide and Assign
// %=  Modulus and Assign
// **= Exponentiate and Assign